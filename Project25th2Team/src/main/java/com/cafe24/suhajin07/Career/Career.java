package com.cafe24.suhajin07.Career;

public class Career {

	
	private String memberCode;
	private String careerName;
	private String careerAddress;
	private String careerEntrance;
	private String careerQuit;
	private String careerWorking;
	private String careerPeriod;
	private String careerPosition;
	private String careerApproval;
	private String careerDate;
	public String getMemberCode() {
		return memberCode;
	}
	public void setMemberCode(String memberCode) {
		this.memberCode = memberCode;
	}
	public String getCareerName() {
		return careerName;
	}
	public void setCareerName(String careerName) {
		this.careerName = careerName;
	}
	public String getCareerAddress() {
		return careerAddress;
	}
	public void setCareerAddress(String careerAddress) {
		this.careerAddress = careerAddress;
	}
	public String getCareerEntrance() {
		return careerEntrance;
	}
	public void setCareerEntrance(String careerEntrance) {
		this.careerEntrance = careerEntrance;
	}
	public String getCareerQuit() {
		return careerQuit;
	}
	public void setCareerQuit(String careerQuit) {
		this.careerQuit = careerQuit;
	}
	public String getCareerWorking() {
		return careerWorking;
	}
	public void setCareerWorking(String careerWorking) {
		this.careerWorking = careerWorking;
	}
	public String getCareerPeriod() {
		return careerPeriod;
	}
	public void setCareerPeriod(String careerPeriod) {
		this.careerPeriod = careerPeriod;
	}
	public String getCareerPosition() {
		return careerPosition;
	}
	public void setCareerPosition(String careerPosition) {
		this.careerPosition = careerPosition;
	}
	public String getCareerApproval() {
		return careerApproval;
	}
	public void setCareerApproval(String careerApproval) {
		this.careerApproval = careerApproval;
	}
	public String getCareerDate() {
		return careerDate;
	}
	public void setCareerDate(String careerDate) {
		this.careerDate = careerDate;
	}
	@Override
	public String toString() {
		return "Career [memberCode=" + memberCode + ", careerName=" + careerName + ", careerAddress=" + careerAddress
				+ ", careerEntrance=" + careerEntrance + ", careerQuit=" + careerQuit + ", careerWorking="
				+ careerWorking + ", careerPeriod=" + careerPeriod + ", careerPosition=" + careerPosition
				+ ", careerApproval=" + careerApproval + ", careerDate=" + careerDate + "]";
	}
	
	
	
}
